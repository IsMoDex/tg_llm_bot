from collections import defaultdict
from utils.logger import logger

# –•—Ä–∞–Ω–∏–ª–∏—â–µ: user_id -> list[dict(role, content)]
user_history = defaultdict(list)

def add_message(user_id: int, role: str, content: str):
    """–î–æ–±–∞–≤–ª—è–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –∏—Å—Ç–æ—Ä–∏—é –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è."""
    if role not in {"system", "user", "assistant"}:
        logger.warning(f"‚ö†Ô∏è –ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è —Ä–æ–ª—å: {role}")
        return
    user_history[user_id].append({"role": role, "content": content})
    logger.debug(f"üí¨ [{role}] {user_id}: {content}")

def get_history(user_id: int, limit: int = 20) -> list[dict]:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø–æ—Å–ª–µ–¥–Ω–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ —Ñ–æ—Ä–º–∞—Ç–µ [{role, content}, ...]."""
    history = user_history[user_id][-limit:]
    logger.debug(f"üìú –ò—Å—Ç–æ—Ä–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}: {history}")
    return history

def clear_history(user_id: int):
    """–û—á–∏—â–∞–µ—Ç –∏—Å—Ç–æ—Ä–∏—é —Å–æ–æ–±—â–µ–Ω–∏–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è."""
    user_history[user_id].clear()
